    private void loginActionPerformed(java.awt.event.ActionEvent evt) throws NoSuchAlgorithmException, UnsupportedAudioFileException, LineUnavailableException, IOException {                                      
               
        Signin signin = new Signin();
        User loggedInUser = null;
        Manager manager = new Manager();
        
        String username = userNameValue.getText();
        String signinPassword = String.valueOf(passwordValue.getPassword());
        String encryptedSigninPassword = SHA256.getSHA(signinPassword);

        HashMap<String, String> response = signin.signin(username, encryptedSigninPassword);

        if (response.containsKey("status")) {
            if (response.get("status").equals("success")) {
                manager.loadAllData();
                //find the username in users list of the manager
                loggedInUser = manager.getLoggedInUser(username);
                
                System.out.println(response.get("message"));
                System.out.println(loggedInUser);

                //Check if employee or customer
                if(loggedInUser.getIsEmployee() == 1 && loggedInUser.getIsAdmin() == 1){
                    BankerHome banker = new BankerHome(this, true, loggedInUser);
                    banker.setVisible(true);
                }
                else{
                    CustomerHome customer = new CustomerHome(this, true, loggedInUser);
                    customer.setVisible(true);
                }

            } else {
                System.out.println(response.get("message"));
                Music.playSound("src/main/java/org/backend/music/invalid.wav");
                JOptionPane.showMessageDialog(null, response.get("message"));
            }
        } else {
            System.out.println("Username does not exist");
            Music.playSound("src/main/java/org/backend/music/invalid.wav");
            JOptionPane.showMessageDialog(null, "Username does not exist");
        }

        this.dispose();
    }

