    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) throws LineUnavailableException, IOException, UnsupportedAudioFileException {                                             
        String currencyUnit = "";
        if(!isDouble(accountBalanceValue.getText().toString())){
            Music.playSound("src/main/java/org/backend/music/invalid.wav");
            JOptionPane.showMessageDialog(null, "Please enter a valid amount");
            return;
        }

        double accountBalance = Integer.parseInt(accountBalanceValue.getText());
        if(accountBalance < Data.initialCheckingAccountBalance){
            Music.playSound("src/main/java/org/backend/music/invalid.wav");
            JOptionPane.showMessageDialog(null, "The minimum balance has to be "+Data.initialCheckingAccountBalance);
            return;
        }
        System.out.println(accountBalance);

        if(usd.isSelected()){
            System.out.println("Currency: USD");
            currencyUnit = usd.getText().toString();
        }
        else if(euro.isSelected()){
            System.out.println("Currency: EUR");
            currencyUnit = euro.getText().toString();
        }
        else if(inr.isSelected()){
            System.out.println("Currency: INR");
            currencyUnit = inr.getText().toString();
        }
        else{
            System.out.println("Currency: Null");
            JOptionPane.showMessageDialog(null, "Please select a currency");
            return;
        }

        Manager manager = new Manager();

        if (loggedInUserGlobal != null) {
            boolean createCheckingAccountSuccess = false;
            System.out.println("Enter Account Details");
            System.out.print("Account Balance: ");

            List<String> accountNumbers = manager.getAccountNumbers();
            
            Account checkingAccount = new AccountChecking(loggedInUserGlobal.getId(),Data.AccountTypes.CHECKING.toString(), accountBalance, 1);
            createCheckingAccountSuccess = checkingAccount.createAccount(loggedInUserGlobal, accountNumbers, currencyUnit);
            if (createCheckingAccountSuccess) {
                System.out.println("Account created successfully");
                Music.playSound("src/main/java/org/backend/music/success.wav");
                JOptionPane.showMessageDialog(null, "Account created successfully");
                manager.loadAllData();
                loggedInUserGlobal = manager.getLoggedInUser(loggedInUserGlobal.getId());
            } else {
                System.out.println("Something went wrong. Account creation failed.");
            }
        } else {
            System.out.println("Please login first");
        }

        //Thread.sleep(1000);
        this.dispose();
    }                                            

