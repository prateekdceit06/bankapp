    private void transferFundsActionPerformed(java.awt.event.ActionEvent evt) throws LineUnavailableException, IOException, UnsupportedAudioFileException {                                              
        
        if(accountTable.getSelectionModel().isSelectionEmpty()){
            System.out.println("Please select the from account number");
            JOptionPane.showMessageDialog(null, "Please select the FROM account number");
            return;
        }

        if(beneficiaryTable.getSelectionModel().isSelectionEmpty()){
            System.out.println("Please select the from account number");
            JOptionPane.showMessageDialog(null, "Please select the TO account number");
            return;
        }

        //get the senders account number
        DefaultTableModel senderModel = (DefaultTableModel)accountTable.getModel();
        int selectedAccount1 = accountTable.getSelectedRow();
        String fromAccountNumber = senderModel.getValueAt(selectedAccount1, 1).toString();

        //get the receivers account number
        DefaultTableModel receiverModel = (DefaultTableModel)beneficiaryTable.getModel();
        int selectedAccount2 = beneficiaryTable.getSelectedRow();
        String toAccountNumber = receiverModel.getValueAt(selectedAccount2, 1).toString();


        if(!isDouble(enterAmountValue.getText().toString())){
            JOptionPane.showMessageDialog(null, "Please enter a valid amount");
            return;
        }
        double amount = Double.parseDouble(enterAmountValue.getText());

        

        //------------------------transfer funds logic----------------------------//

        if (loggedInUserGlobal != null) {
            System.out.println();
            System.out.println("sender account number: "+ fromAccountNumber);
            System.out.println("transfer amount: "+ amount);
            System.out.println("receiver account number: "+ toAccountNumber);
            
            //find account in manager accounts

            Manager manager = new Manager();
            manager.loadAllData();
            loggedInUserGlobal = manager.getLoggedInUser(loggedInUserGlobal.getId());
            
            if (manager.getAccounts() != null && manager.getAccounts().size() > 0
                    && manager.getAccountNumbers().contains(fromAccountNumber) &&
                    manager.getAccountNumbers().contains(toAccountNumber)) {
                for (AccountSavings accountSavings : manager.getSavingsAccounts()) {
                    if (accountSavings.getCustomerId() == loggedInUserGlobal.getId() 
                        &&
                        accountSavings.getAccountNumber().equals(fromAccountNumber)) {

                        System.out.println("inside savings transfer");
                            
                        //transfer money
                        boolean transferMoneySuccess = accountSavings.transfer(amount,
                                fromAccountNumber, toAccountNumber, loggedInUserGlobal);

                        if (transferMoneySuccess) {
                            System.out.println("Money transferred successfully");
                            Music.playSound("src/main/java/org/backend/music/success.wav");
                            JOptionPane.showMessageDialog(null, "Money transferred successfully");
                        } else {
                            System.out.println("Something went wrong. Money transfer failed.");
                            Music.playSound("src/main/java/org/backend/music/invalid.wav");
                            JOptionPane.showMessageDialog(null, "Something went wrong. Money transfer failed. - 1");
                        }
                    }
                }
                for (AccountChecking checkingAccount : manager.getCheckingAccounts()) {
                    if (checkingAccount.getCustomerId() == loggedInUserGlobal.getId() 
                        &&
                        checkingAccount.getAccountNumber().equals(fromAccountNumber)) {

                        System.out.println("inside checkings transfer");

                        //transfer money
                        boolean transferMoneySuccess = checkingAccount.transfer(amount,
                                fromAccountNumber, toAccountNumber, manager.getBankAccountNumber(),
                                loggedInUserGlobal);

                        if (transferMoneySuccess) {
                            System.out.println("Money transferred successfully");
                            Music.playSound("src/main/java/org/backend/music/success.wav");
                            JOptionPane.showMessageDialog(null, "Money transferred successfully");
                        } else {
                            System.out.println("Something went wrong. Money transfer failed.");
                            Music.playSound("src/main/java/org/backend/music/invalid.wav");
                            JOptionPane.showMessageDialog(null, "Something went wrong. Money transfer failed. - 2");
                        }
                    }
                }
                manager.loadAllData();
                loggedInUserGlobal = manager.getLoggedInUser(loggedInUserGlobal.getId());
            } else {
                System.out.println("No Such Account Found");
                JOptionPane.showMessageDialog(null, "No Such Account Found");
            }
        } else {
            System.out.println("Please login first");
        }

        this.dispose();

        //--------------------End transfer funds logic----------------------------//

    }                                             

